using System;

namespace Strings
{
    class StringLength
    {
        static void Main(string[] args)
        {
            String Name1 = "Akshay";
            String Name2 = "Ishwarya";
            Console.WriteLine($"The Name {Name1} has {Name1.Length} letters");
            Console.WriteLine($"The Name {Name2} has {Name2.Length} letters");
        }
    }
}
